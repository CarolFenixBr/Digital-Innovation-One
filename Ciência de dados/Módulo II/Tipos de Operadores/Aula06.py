#Operadores aritméticos
produto_A = 15
produto_B = 30

print(produto_A + produto_B)  #adição
print(produto_A - produto_B)  #subtração
print(produto_A / produto_B)  #divisão
print(produto_A // produto_B) #divisão inteira
print(produto_A * produto_B)  #multiplicação
print(produto_A % produto_B)  #módulo
print(produto_A ** produto_B) #exponenciação

x = (10 + 5) * 4
y = (10 / 2) + (25 * 2) - (2 ** 2)
print(x)
print(y)
